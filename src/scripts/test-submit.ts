    reason_for_therapy: 'Testing the intake form PDF and email pipeline. This is a test submission — please ignore.',
    diagnosed_conditions: 'None (test)',
    previous_therapy: 'yes',
    previous_therapy_details: 'Worked with a therapist for 6 months in 2022.',
    current_medication: 'None',

    // Step 4 — Other Support
    seeing_psychiatrist: 'no',
    psychiatrist_details: '',
    uses_ai_tools: 'sometimes',

    // Step 5 — Emergency Contact
    emergency_contact_name: 'Jane Test',
    emergency_contact_phone: '+353 87 111 1111',
    emergency_contact_relationship: 'Partner',

    // GP Details
    gp_name: 'Dr. Test GP',
    gp_practice: 'Test Medical Centre, Dublin',


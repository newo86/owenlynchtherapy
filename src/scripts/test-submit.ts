  if (!submitRes.ok) {
    console.error('❌  Submission failed:', submitRes.status, submitRes.json);
    process.exit(1);
  }

  console.log('✅  Form submitted successfully');
  console.log('\n📧  Check the terminal running the dev server for email/PDF logs.');
  console.log('📋  Check /admin/intake — the submission should appear in the "Submitted forms" table.\n');
